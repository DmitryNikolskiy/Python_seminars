X = int(input('Введите координату X: '))
Y = int(input('Введите координату Y: '))

if X > 0 and Y > 0: print('Точка находится в первой четверти')
elif X < 0 and Y > 0: print('Точка находится во второй четверти')
elif X < 0 and Y < 0: print('Точка находится в третьей четверти')
elif X > 0 and Y < 0: print('Точка находится в четвертой четверти')
elif X == 0 and Y != 0: print('Точка находится на оси Х')
elif Y == 0 and X != 0: print('Точка находится на оси Y')
else: print('Точка находится на пересечении осей X и Y')